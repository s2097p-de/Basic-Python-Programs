# 6. Write a program which calculates basics operations between two numbers.

# taking User Input
no1 = float(input("Enter the First number :  "))
no2 = int(input("Enter the second number :  "))

## calculates basics operations between two numbers

# Addition
add = no1 + no2
print(f"Addition of two number is {add}.")

# Subtraction
sub = no1 - no2
print(f"Subtraction of two number is {sub}.")

# Multiplication
mul = no1*no2
print(f"Multiplication of two number is {mul}.")

#Division
div = no1 / no2
print(f"Division of two number is {div}.")

# Modulo Operater
mod = no1 // no2
print(f"Modulo of two number is {mod}.")

# Square
sq = no1 ** no2 
print(f"Square of two number is {sq}.")